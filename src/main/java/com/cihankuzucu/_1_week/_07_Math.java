package com.cihankuzucu._1_week;

public class _07_Math {

    public static void main(String[] args) {

        System.out.println("En küçük: "+Math.min(-300,10));
        System.out.println("En büyük: "+Math.max(-300,10));

        System.out.println("Mutlak değer: "+Math.abs(-10));

        System.out.println("Karekök: "+Math.sqrt(25));
        System.out.println("Karekök: "+Math.sqrt(+25));
        System.out.println("Karekök: "+Math.sqrt(-25));
        System.out.println("Karekök: "+Math.sqrt(Math.abs(-25)));

        System.out.println("Üslü Sayı: "+Math.pow(2,5));

        System.out.println("Aşağı Yuvarla: "+Math.floor(2.9));
        System.out.println("Yukarı Yuvarla: "+Math.ceil(2.1));
        System.out.println("Yukarı Yuvarla: "+Math.ceil(2.00001));
        System.out.println("Ortalama Yuvarla: "+Math.round(3.4));
        System.out.println("Ortalama Yuvarla: "+Math.round(3.5));
        System.out.println("Ortalama Yuvarla: "+Math.round(3.6));

        System.out.println("Sin:  "+Math.sin(1));
        System.out.println("Cos:  "+Math.cos(1));
        System.out.println("Tan:  "+Math.tan(1));

        System.out.println("PI sayısı: "+Math.PI);





    }
}
